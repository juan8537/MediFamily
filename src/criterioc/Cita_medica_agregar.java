    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        try{
        String cita = txt_cita.getText().trim();
        String fecha = txt_fecha.getText().trim();
        String hora = txt_hora.getText().trim();
        String doctor = txt_medico.getText().trim();
        String paciente = txt_paciente.getText().trim();
        String ruta_imagen = lbl_foto.getIcon() != null ? ((ImageIcon) lbl_foto.getIcon()).getDescription() : "";
        
        String[] partesFecha = fecha.split("/"); //validacion de la fecha
        if (partesFecha.length != 3) {
            JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto, utilise d/M/y");
            return;
        }
        String fechaMySQL = partesFecha[2] + "-" + partesFecha[1] + "-" + partesFecha[0];
        
        insertar_metodo1(cita, fechaMySQL, hora, doctor, paciente, ruta_imagen);
        JOptionPane.showMessageDialog(this, "Cita guardada exitosamente");//para verificar que todo este bien
        Cita_medica mostrarCita = new Cita_medica();
            mostrarCita.mostrarDatos(cita, fecha, hora, doctor, paciente, ruta_imagen);
            mostrarCita.setVisible(true);
    } catch (Exception e) {
        
        
        JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
        }
        
            
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_imagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imagenActionPerformed
        // TODO add your handling code here:
        
        JFileChooser fileChooser = new JFileChooser();
    fileChooser.setFileFilter(new FileNameExtensionFilter("Im√°genes", "jpg", "png", "jpeg"));
    
    // Abre el di√°logo para seleccionar imagen
    int resultado = fileChooser.showOpenDialog(this);
    
    // selecciona una imagen
    if (resultado == JFileChooser.APPROVE_OPTION) {
        File archivoImagen = fileChooser.getSelectedFile();
        try {
            //  Carga la imagen 
            ImageIcon icono = new ImageIcon(archivoImagen.getAbsolutePath());
            Image imagen = icono.getImage().getScaledInstance(
                lbl_foto.getWidth(), 
                lbl_foto.getHeight(), 
                Image.SCALE_SMOOTH
            );
            //  Muestra la imagen en el lbl
            lbl_foto.setIcon(new ImageIcon(imagen));
            
            
            ((ImageIcon)lbl_foto.getIcon()).setDescription(archivoImagen.getAbsolutePath());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la imagen", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    }//GEN-LAST:event_btn_imagenActionPerformed

