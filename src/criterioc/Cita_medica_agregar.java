    public static void insertar_metodo1 (String Numero_cita, String Fecha, String Hora, String Doctor, String Usuario_Nombre, String ruta_imagen)
    {
        ConeccionBD BD = null;
        try
        {
            BD = new ConeccionBD ("jdbc:mysql://"+servidor+":3306/"+basededatos+"?useSSL=false"
                                + "&characterEncoding=utf-8",usuario,contrasena);
            BD.getNewConnection();
            try
            {
                Statement stQueryInsert = BD.getCurrentConnection().createStatement();
                String queryInsert = "INSERT INTO cita_medica (Numero_cita, Fecha, Hora, Doctor, Usuario_Nombre, ruta_imagen) " +
                     "VALUES ('" + Numero_cita+ "','" + Fecha + "','" + Hora + "' , '"+ Doctor +"' , '"+ Usuario_Nombre +"' , '"+ ruta_imagen +"')";
                
                stQueryInsert.executeUpdate(queryInsert);
            } catch (Exception e1) {
                JOptionPane.showMessageDialog(null, "Error al insertar: " + e1.getMessage());
                e1.printStackTrace();
            } finally {
                BD.closeConnection();
            }
        } catch (Exception e0) {
            JOptionPane.showMessageDialog(null, "Error de conexi√≥n: " + e0.getMessage());
            e0.printStackTrace();
        }
    }
    
    
