    public static void insertar_metodo1 (String Nombre_medicamento, String Hora, String Dosis, 
        String Usuario_nombre, String rutaImagen, int Frecuencia) {
    ConeccionBD BD = null;
    try {
        BD = new ConeccionBD ("jdbc:mysql://"+servidor+":3306/"+basededatos+"?useSSL=false"
                            + "&characterEncoding=utf-8",usuario,contrasena);
        BD.getNewConnection();
        Statement stQueryInsert = BD.getCurrentConnection().createStatement();
        String queryInsert = "INSERT INTO medicamentos (Nombre_medicamento, Hora, Dosis, Usuario_Nombre, "
                + "imagen, Frecuencia) " +
             "VALUES ('" + Nombre_medicamento + "','" + Hora + "','" + Dosis + "',"
                + "'" + Usuario_nombre + "','" + (rutaImagen != null ? rutaImagen : "") + "'," + Frecuencia + ")";
        stQueryInsert.executeUpdate(queryInsert);
        BD.closeConnection();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al insertar el medicamento:\n" + e.getMessage());
    }
}
    
