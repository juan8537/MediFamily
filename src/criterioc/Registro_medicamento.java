    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        // TODO add your handling code here:
        String nombre = txt_nombre.getText().trim();
        String dosis = txt_dosis.getText().trim();
        String horario = txt_horario.getText().trim();
        String paciente = txt_paciente.getText().trim();
        String frecuenciaStr = txt_frecuencia.getText().trim();
        
    if (nombre.isEmpty() || dosis.isEmpty() || horario.isEmpty() || 
            paciente.isEmpty() || frecuenciaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos obligatorios.");
            return;
        }
    
        int frecuencia ;
        
        try {
            frecuencia = Integer.parseInt(frecuenciaStr);
            if (frecuencia < 1 || frecuencia > 24) {
    JOptionPane.showMessageDialog(this, "La frecuencia debe estar entre 1 y 24 horas");
    return;
    }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La frecuencia debe ser un número entero válido.");
            return;
        } 
        
        insertar_metodo1(nombre, horario, dosis, paciente, rutaImagen, frecuencia);
        
        JOptionPane.showMessageDialog(this, "Medicamento registrado correctamente.");
        limpiarCampos();
        
    }//GEN-LAST:event_btn_ingresarActionPerformed

