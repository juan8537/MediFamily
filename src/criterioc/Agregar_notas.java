    public static void insertar_metodo1 (String fecha, String notas, String cita)
    {
        ConeccionBD BD = null;
        try
        {
            BD = new ConeccionBD ("jdbc:mysql://"+servidor+":3306/"+basededatos+"?useSSL=false"
                                + "&characterEncoding=utf-8",usuario,contrasena);
            BD.getNewConnection();
            try
            {
                Statement stQueryInsert = BD.getCurrentConnection().createStatement();
                String queryInsert = "INSERT INTO notas_medicas (Fecha_cita, Notas, Numero_Cita) " +
                     "VALUES ('" + fecha + "','" + notas + "','" + cita + "')";
                stQueryInsert.executeUpdate(queryInsert);
                BD.closeConnection();
            }
            catch (Exception e1)
            {
                e1.printStackTrace();
            }
        }
        catch (Exception e0)
        {
            e0.printStackTrace();
        }
    }
        
