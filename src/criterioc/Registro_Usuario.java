    public static void insertar_metodo1 (String Nombre, String Edad, String Peso, String Altura, String Correo, String ruta_imagen)
    {
        ConeccionBD BD = null;
        try
        {
            BD = new ConeccionBD ("jdbc:mysql://"+servidor+":3306/"+basededatos+"?useSSL=false"
                                + "&characterEncoding=utf-8",usuario,contrasena);
            BD.getNewConnection();

            try
            {
                Statement stQueryInsert = BD.getCurrentConnection().createStatement();
                String queryInsert = "INSERT INTO usuario (Nombre, Edad, Peso, Altura, Correo,ruta_imagen )\n" +
                                    "VALUES ('"+Nombre+"','"+Edad+"','"+Peso+"','"+Altura+"','"+Correo+"','"+ruta_imagen+"')";
                stQueryInsert.executeUpdate(queryInsert);
                BD.closeConnection();
            }
            catch (Exception e1)
            {
                e1.printStackTrace();
            }
        }
        catch (Exception e0)
        {
            e0.printStackTrace();
        }
    }


