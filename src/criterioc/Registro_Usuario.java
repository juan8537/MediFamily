    private void btn_imagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_imagenActionPerformed
        // TODO add your handling code here:
                                                  
    
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setFileFilter(new FileNameExtensionFilter("Imágenes", "jpg", "png", "jpeg"));
    
    // Abre el diálogo para seleccionar imagen
    int resultado = fileChooser.showOpenDialog(this);
    
    // selecciona una imagen
    if (resultado == JFileChooser.APPROVE_OPTION) {
        File archivoImagen = fileChooser.getSelectedFile();
        try {
            //  Carga la imagen 
            ImageIcon icono = new ImageIcon(archivoImagen.getAbsolutePath());
            Image imagen = icono.getImage().getScaledInstance(
                lbl_foto.getWidth(), 
                lbl_foto.getHeight(), 
                Image.SCALE_SMOOTH
            );
            // Muestra la imagen en el lbl
            lbl_foto.setIcon(new ImageIcon(imagen));
            
            
            ((ImageIcon)lbl_foto.getIcon()).setDescription(archivoImagen.getAbsolutePath());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar la imagen", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

        
    }//GEN-LAST:event_btn_imagenActionPerformed

