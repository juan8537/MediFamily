    public static void asignarMedicamento(String usuarioNombre, String nombreMedicamento, java.util.Date fechaInicio, java.util.Date fechaFin) {
     ConeccionBD BD = null;
    try {
        BD = new ConeccionBD("jdbc:mysql://" + servidor + ":3306/" + basededatos + "?useSSL=false&characterEncoding=utf-8", usuario, contrasena);
        BD.getNewConnection();
        Statement st = BD.getCurrentConnection().createStatement();

        // Convertir fechas a formato SQL
        java.sql.Date sqlFechaInicio = new java.sql.Date(fechaInicio.getTime());
        java.sql.Date sqlFechaFin = new java.sql.Date(fechaFin.getTime());

        String query = "INSERT INTO asignacion_medicamento (Usuario_Nombre, Nombre_medicamento, Fecha_inicio, Fecha_final) VALUES ("
                     + "'" + usuarioNombre + "', "
                     + "'" + nombreMedicamento + "', "
                     + "'" + sqlFechaInicio + "', "
                     + "'" + sqlFechaFin + "')";
        st.executeUpdate(query);
        BD.closeConnection();
        JOptionPane.showMessageDialog(null, "Â¡Medicamento asignado correctamente!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al asignar medicamento:\n" + e.getMessage());
    }
}
    
    private void cargarUsuarios() {
    cbx_usuario.removeAllItems();
    try {
        ConeccionBD BD = new ConeccionBD("jdbc:mysql://" + servidor + ":3306/" + basededatos + "?useSSL=false&characterEncoding=utf-8", usuario, contrasena);
        BD.getNewConnection();
        Statement st = BD.getCurrentConnection().createStatement();
        ResultSet rs = st.executeQuery("SELECT Nombre FROM usuario");

        while (rs.next()) {
            cbx_usuario.addItem(rs.getString("Nombre"));
        }

        BD.closeConnection();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios:\n" + e.getMessage());
    }
}

private void cargarMedicamentos() {
    cbx_medicamento.removeAllItems();
    try {
        ConeccionBD BD = new ConeccionBD("jdbc:mysql://" + servidor + ":3306/" + basededatos + "?useSSL=false&characterEncoding=utf-8", usuario, contrasena);
        BD.getNewConnection();
        Statement st = BD.getCurrentConnection().createStatement();
        ResultSet rs = st.executeQuery("SELECT Nombre_medicamento FROM medicamentos");

        while (rs.next()) {
            cbx_medicamento.addItem(rs.getString("Nombre_medicamento"));
        }

        BD.closeConnection();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar medicamentos:\n" + e.getMessage());
    }
}
    
